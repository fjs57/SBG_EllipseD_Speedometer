from enum import IntFlag


class ComStatus(IntFlag):
    PORT_A_TX_READY  = 0x00000001
    PORT_A_RX_READY  = 0x00000002
    PORT_A_RX_OK     = 0x00000004
    PORT_B_TX_READY  = 0x00000008
    PORT_B_RX_READY  = 0x00000010
    PORT_B_RX_OK     = 0x00000020
    PORT_C_TX_READY  = 0x00000040
    PORT_C_RX_READY  = 0x00000080
    PORT_C_RX_OK     = 0x00000100
    PORT_D_TX_READY  = 0x00000200
    PORT_D_RX_READY  = 0x00000400
    PORT_D_RX_OK     = 0x00000800
    PORT_E_TX_READY  = 0x00001000
    PORT_E_RX_READY  = 0x00002000
    PORT_E_RX_OK     = 0x00004000
    ETH_TX_READY     = 0x00008000
    ETH_RX_READY     = 0x00010000
    ETH_RX_OK        = 0x00020000
    CAN_TX_READY     = 0x00040000
    CAN_RX_READY     = 0x00080000
    CAN_RX_OK        = 0x00100000


class ComStatus2(IntFlag):
    ETH0_TX_READY  = 0x0001
    ETH0_RX_READY  = 0x0002
    ETH0_RX_OK     = 0x0004
    ETH1_TX_READY  = 0x0008
    ETH1_RX_READY  = 0x0010
    ETH1_RX_OK     = 0x0020
